	int i; //32bit = 4byte 시스템에 따라서 자동 결정이지만, 대체로 long형태 -2^31~2^31-1
	long l; // 4byte
	long long ll; // 8byte

	//실수형
	float f; // 4byte
	double d; // 8byte

	//
	unsigned int ui; //2^32 -1 
	
